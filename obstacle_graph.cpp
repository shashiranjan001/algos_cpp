#include <bits/stdc++.h>
using namespace std;

struct ComparisonClass {
    bool operator() (vector<long long> a, vector<long long> b) {
        if(b[0]<a[0]) return true;
        return false;
    }
};

int min_path(vector<vector<int>> &grid, int x, int y)
{
    int n=grid.size();
    int m=grid[0].size();
    vector<vector<long long>> dis(n, vector<long long>(m,INT_MAX));

    priority_queue<vector<long long>, vector<vector<long long>>, ComparisonClass> pq;
    pq.push({0,x,y});
    dis[x][y]=0;

    while(!pq.empty())
    {
        vector<long long> curr = pq.top();
        pq.pop();
        int cx = curr[1], cy=curr[2];
        if(cx==n-1 && cy==m-1) return dis[cx][cy];
        int cd = dis[cx][cy];
        if(cx>0 && grid[cx-1][cy] == 0)
        {
            if(dis[cx-1][cy] > dis[cx][cy]+1)
            {
                dis[cx-1][cy] = dis[cx][cy]+1;
                pq.push({dis[cx-1][cy], cx-1, cy});
            }
        }

        if(cx<n-1 && grid[cx+1][cy] == 0)
        {
            if(dis[cx+1][cy] > dis[cx][cy]+1)
            {
                dis[cx+1][cy] = dis[cx][cy]+1;
                pq.push({dis[cx+1][cy], cx+1, cy});
            }
        }

        if(cy>0 && grid[cx][cy-1] == 0)
        {
            if(dis[cx][cy-1] > dis[cx][cy]+1)
            {
                dis[cx][cy-1] = dis[cx][cy]+1;
                pq.push({dis[cx][cy-1], cx, cy-1});
            }
        }


        if(cy<n-1 && grid[cx][cy+1] == 0)
        {
            if(dis[cx][cy+1] > dis[cx][cy]+1)
            {
                dis[cx][cy+1] = dis[cx][cy]+1;
                pq.push({dis[cx][cy+1], cx, cy+1});
            }
        }
    }
    return dis[n-1][m-1];
}


int main()
{
    int n, m;
    cin>>n>>m;
    cout<<"n = "<<n<<" m = "<<m<<endl;
    vector<vector<int>> grid(n, vector<int>(m,0));
    cout<<"Hi\n";
    int t;
    for(int i=0; i<n; i++)
    {
        for(int j=0; j<m; j++)
        {   cin>>t; grid[i][j]=t;}
    }

    for(int i=0; i<n; i++)
    {
        for(int j=0; j<m; j++)
            cout<<grid[i][j]<<" ";
        cout<<endl;
    }
    cout<<"--------------\n";


    int res = min_path(grid, 0, 0);
    cout<<res<<endl;
}

